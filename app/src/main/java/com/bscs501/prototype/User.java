package com.bscs501.prototype;

public class User {
    public String firstname, middlename, lastname, email, age;

    public User(){

    }

    public User(String firstname, String middlename, String lastname, String email, String age){
        this.firstname = firstname;
        this.middlename = middlename;
        this.lastname = lastname;
        this.email = email;
        this.age = age;

    }
}
